#include "libft.h"

int	ft_atoi(const char *str)
{
	int	s;
	int	n;

	s = 1;
	n = 0;
	while ((*str >= 9 && *str <= 13) || *str == 32)
	{
		str++;
	}
	if (*str == 45 || *str == 43)
	{
		if (*str == 45)
			s = -1;
		str++;
	}
	while (*str >= 48 && *str <= 57)
	{
		n = n * 10 + (*str - 48);
		str++;
	}
	return ((int)(s * n));
}
